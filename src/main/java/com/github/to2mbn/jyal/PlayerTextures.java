package com.github.to2mbn.jyal;

public class PlayerTextures {

	private Texture skin;
	private Texture cape;

	public PlayerTextures(Texture skin, Texture cape) {
		this.skin = skin;
		this.cape = cape;
	}

	public Texture getSkin() {
		return skin;
	}

	public Texture getCape() {
		return cape;
	}

}
